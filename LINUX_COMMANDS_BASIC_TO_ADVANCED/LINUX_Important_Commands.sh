" The following commands are most important and useful for daily working in LINUX."

* pwd :
        It stands for 'print working directory'. It's a built-in command that displays the full
        path-name of the current directory in the terminal.

    For example :   [root@ip-172-31-35-*** LINUX_COMMANDS_BASIC_TO_ADVANCED]# pwd
                    /root/Complete_LINUX_Administration/LINUX_COMMANDS_BASIC_TO_ADVANCED

